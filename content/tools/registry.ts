export const tools: ResolvedTool[] = toolList.map(resolveTool);

const bySlug = new Map(toolList.map((t) => [t.slug, t]));

export function getToolBySlug(slug: string): ResolvedTool | undefined {
  const raw = bySlug.get(slug);
  return raw ? resolveTool(raw) : undefined;
}

/** 원본(기본값 미적용) — 편집·테스트용 */
export function getRawToolBySlug(slug: string): ToolDefinition | undefined {
  return bySlug.get(slug);
}

export function getToolsByCategory(categoryId: CategoryId): ResolvedTool[] {
  return tools.filter((t) => t.categoryId === categoryId);
}

export function getAllToolSlugs(): string[] {
  return tools.map((t) => t.slug);
}
